uint32_t OSTimeGet()
{
	rt_tick_t tick = 0;
  tick = rt_tick_get();
	if(tick <RT_TICK_PER_SECOND) return 0;
	return tick/RT_TICK_PER_SECOND;
}

uint32_t OSTimeGetMS(){
	rt_tick_t tick = 0;
	tick = rt_tick_get();
	if(tick <(rt_tick_from_millisecond(1))) return 0;
	return tick*1000/(RT_TICK_PER_SECOND);
}

