unsigned int power(int base, int times) 
{
	int i;
	unsigned int rusult = 1;
	
	for(i=0;i < times; i++)
		rusult *= base;

	return rusult;
}

/*
* 十进制转化为bcd  码
* @dec : 十进制的值
* @buf : 存放bcd  的缓存
* @len : bu  存放bcd缓存的长度
* @return : buf  中的有效数据开始位置
*/
int decimal2bcd(unsigned int dec,unsigned char *buf, int len)
{
	int i;
	unsigned temp;

	//DbgPrintf("dec = %d\r\n",dec);
	//DbgPrintf("len = %d\r\n",len);

	memset(buf,0x00,len);
	
	for(i=len-1;i>=0;i--)
	{
		if(dec == 0)
			break;
			
		temp = dec % 100;		
		buf[i] = ((temp/10) << 4) | ((temp % 10) & 0x0F);   
		dec /= 100;


		//DbgPrintf("temp = %d\r\n",temp);
		//DbgPrintf("dec = %d\r\n",dec);
		//DbgPrintf("val = %d\r\n",buf[i]);

	}
	
	return i+1;
}

/*
* bcd码转化为十进制
* @buf : 存放bcd的缓存
* @len : 存放bcd缓存的长度
* @return : 返回十进制的数据
*/
unsigned int bcd2decimal(unsigned char *buf, int len)
{
	int	i;
	unsigned int tmp;
	unsigned int dec = 0;
	
	for(i=0;i<len;i++)
	{
		tmp = ((buf[i] >> 4) & 0x0F) * 10 + (buf[i]&0x0F);
		dec += tmp * power(100, len-1-i);    
	}

	return dec;
}

bool is_bcd(unsigned char *buf, int len)
{
	int i;
	
	for(i=0; i<len; i++)
	{
		if((((buf[i] >> 4) & 0x0F) > 0x09) || ((buf[i]  & 0x0F) > 0x09))
			return false;
	}

	return true;
}

char __getHexCharForDigit(int val){
	if(val < 10){
		return '0'+val;
	}
	return 55 + val; //A(65)+val-10
}

int __getDigitForHexChar(char ch){
	if(ch >= '0' && ch <= '9'){
		return ch - '0';
	}
	if(ch >= 'A' && ch <= 'F'){
		return ch-'A' + 10;
	}
	if(ch >= 'a' && ch <= 'f'){
		return ch-'a' + 10;
	}
	return -1;
}

int str2BCDByteArray(char *str,unsigned char *bcdByteArray){
	if(!str){
		return 1;
	}
	int len = strlen(str),tempDigit,realNum = 0;
	char *startRecord = str;
	if((len%2) == 1){
		tempDigit = __getDigitForHexChar(*startRecord);
		if(tempDigit < 0){
			return 2;
		}
		*bcdByteArray = tempDigit;
		startRecord +=1;
		len -= 1;
		realNum = 1;
	}
	int num = len/2,val;
	char *record = (char*)bcdByteArray+realNum;
	realNum += num;

	int i;
	for(i=0;i<num;i++){
		tempDigit = __getDigitForHexChar(*startRecord);
		if(tempDigit < 0){
			return -2;
		}
		val = tempDigit << 4;
		tempDigit = __getDigitForHexChar(*(startRecord+1));
		if(tempDigit < 0){
			return -2;
		}
		*record = val + tempDigit;

		record += 1;
		startRecord += 2;
	}

	return realNum;
}
int bcdByteArray2Str(unsigned char *bcdByteArray,int len,char *str){
	if(!bcdByteArray || !str){
		return 1;
	}
	if(len < 0){
		return 2;
	}

	char *temp = (char*)bcdByteArray;
	char *dest = str;
	int i,val;
	for(i=0;i<len;i++){
		val = *temp & 0xFF;

		*(dest++) = __getHexCharForDigit(val/16);
		*(dest++) = __getHexCharForDigit(val%16);

		temp ++;
	}
	*(dest++) = 0;

	return 2*len;
}


void bcdtime_to_char(unsigned char *bcd_time,unsigned char* str_time)
{
	
	DbgFuncEntry();
	unsigned int year,mon,day,hour,min,sec;
	
	year = bcd2decimal(bcd_time++,1);
	mon  = bcd2decimal(bcd_time++,1);
	day  = bcd2decimal(bcd_time++,1);
	hour = bcd2decimal(bcd_time++,1);
	min  = bcd2decimal(bcd_time++,1);
	sec  = bcd2decimal(bcd_time++,1);
	
	sprintf((char*)str_time,"%d-%02d-%02d %02d:%02d:%02d",year+2000,mon,day,hour,min,sec);
	DbgFuncExit();
	return;
}

